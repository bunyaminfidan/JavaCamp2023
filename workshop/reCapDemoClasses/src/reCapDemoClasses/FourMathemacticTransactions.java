package reCapDemoClasses;

public class FourMathemacticTransactions {
	
	public int plus (int number1, int number2) {
		int total = 0;
		total = number1 + number2;
		
		return total;
		
	}
	
	public int minus (int number1, int number2) {
		int total = 0;
		total = number1 - number2;
		
		return total;
		
	}
	
	public int divideBy (int number1, int number2) {
		int total = 0;
		total = number1 / number2;
		
		return total;
		
	}
	
	public int multiply (int number1, int number2) {
		int total = 0;
		total = number1 * number2;
		
		return total;
		
	}

}
